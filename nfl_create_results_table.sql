CREATE TABLE IF NOT EXISTS processedresults (
	GameId int NOT NULL AUTO_INCREMENT,
	Rk INT,
	Season INT,
	GameWeek varchar(30),
	ModifiedGameWeek INT,
	GameDate DATE,
	GameDayOfWeek varchar(15),
	WinnerTie varchar(30),
	LoserTie varchar(60),
	PtsW INT,
	PtsL INT,
	YdsW INT,
	TOW INT,
	YdsL INT,
	TOL INT,
	HomeTeam varchar(30),
	PtsMargin INT,
	TieGame BOOLEAN,
	PRIMARY KEY (GameId),
	INDEX (Season,
	GameWeek,
	ModifiedGameWeek)
);